package littleJ.esp.dto;

public class EspStatusDTO {
	int idEspStatus;
	int idDevice;
	int status;
	
	public int getIdEspStatus() {
		return idEspStatus;
	}
	public void setIdEspStatus(int idEspStatus) {
		this.idEspStatus = idEspStatus;
	}
	public int getIdDevice() {
		return idDevice;
	}
	public void setIdDevice(int idDevice) {
		this.idDevice = idDevice;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	
	

}
